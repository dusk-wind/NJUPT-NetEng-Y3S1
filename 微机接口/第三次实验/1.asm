.MODEL SMALL
.486
CODE SEGMENT USE16
    ASSUME CS:CODE
BEG: JMP START

; 端口定义
CCONPORT EQU 213H ;控制口地址
CDPORT1  EQU 210H ;计数器0口地址
CDPORT2  EQU 211H ;计数器1口地址
CDPORT3  EQU 212H ;计数器2口地址 

; 控制字定义：方式3，只读写低8位，二进制
CCONBIT1 EQU 00010110B 
CCONBIT2 EQU 01010110B 
CCONBIT3 EQU 10010110B 

; 计数初值定义 (修改为互质素数)
CHDBIT1 EQU 37  ; 00100101B
CHDBIT2 EQU 23  ; 00010111B
CHDBIT3 EQU 13  ; 00001101B

START:
    ; --- 初始化计数器0 ---
    MOV DX, CCONPORT
    MOV AL, CCONBIT1
    OUT DX, AL
    MOV DX, CDPORT1
    MOV AL, CHDBIT1
    OUT DX, AL

    ; --- 初始化计数器1 ---
    MOV DX, CCONPORT
    MOV AL, CCONBIT2
    OUT DX, AL
    MOV DX, CDPORT2
    MOV AL, CHDBIT2
    OUT DX, AL

    ; --- 初始化计数器2 ---
    MOV DX, CCONPORT
    MOV AL, CCONBIT3
    OUT DX, AL
    MOV DX, CDPORT3
    MOV AL, CHDBIT3
    OUT DX, AL

WT: JMP WT
CODE ENDS
END BEG
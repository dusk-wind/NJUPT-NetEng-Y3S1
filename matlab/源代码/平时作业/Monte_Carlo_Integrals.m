%% --- 【例 6.4-1】: 基本蒙特卡洛法 (规则单位区域) ---
% ∫₀¹...∫₀¹ e^(x₁x₂x₃x₄) dx₁...dx₄
fprintf('--- 正在求解【例 6.4-1】: 基本蒙特卡洛法 (单位超立方体) ---\n');

f1 = @(x) exp(prod(x, 2)); % 接受 N-by-4 矩阵, 按行求积
n1 = 100000; % 增加采样点以提高精度
X1 = rand(n1, 4); % 生成 N x 4 的随机点矩阵

% 积分 I ≈ V * <f> = (1*1*1*1) * (Σf(p_i) / n)
volume1 = 1;
I1 = volume1 * sum(f1(X1)) / n1;

fprintf('采样点数: %d\n', n1);
fprintf('计算结果: %f\n', I1);
fprintf('书中结果 (n=10000): 1.06984...\n');
fprintf('精确结果 (来自6.3节): 1.06939...\n\n');

%% --- 【例 6.4-2】: 基本蒙特卡洛法 (不规则区域) ---
% ∫₁² dx₁ ∫ₓ₁²ˣ¹ dx₂ ∫ₓ₁ₓ₂²ˣ₁ˣ₂ x₁x₂x₃ dx₃
fprintf('--- 正在求解【例 6.4-2】: 基本蒙特卡洛法 (不规则区域) ---\n');

f2 = @(x) prod(x); % 接受 3-by-N 矩阵, 按列求积
n2 = 1000000; % 增加采样点以提高精度

% 定义包含积分区域的包围盒 (Bounding Box)
xmin = [1, 1, 1];
xmax = [2, 4, 16];
volume2 = prod(xmax - xmin);

% 在包围盒内生成随机点
X2 = xmin' + (xmax' - xmin') .* rand(3, n2);
x1 = X2(1,:);
x2 = X2(2,:);
x3 = X2(3,:);

% 找到落在真实积分区域内的点 (拒绝采样)
ind = (x2 > x1) & (x2 < 2*x1) & (x3 > x1.*x2) & (x3 < 2*x1.*x2);

% 积分 I ≈ V * <f>_valid = V * (Σf(p_valid) / n_total)
I2 = volume2 * sum(f2(X2(:, ind))) / n2;

fprintf('采样点数: %d\n', n2);
fprintf('计算结果: %.4f\n', I2);
fprintf('书中结果 (n=100000): 179.0310\n');
fprintf('精确结果 (来自6.3节): 179.2969\n\n');

%% --- 【例 6.4-3】: 等分布序列蒙特卡洛法 (单位区域) ---
% 与 6.4-1 相同的问题，但使用确定性的“准随机”序列
fprintf('--- 正在求解【例 6.4-3】: 等分布序列蒙特卡洛法 ---\n');

f3 = @(x) exp(prod(x)); % 接受 4-by-N 矩阵
n3 = 10000;

% 使用 Halton 序列，这是比书中更现代、更标准的准随机序列生成方法
p = haltonset(4, 'Skip', 1, 'Leap', 0);
p = scramble(p, 'RR2');
X3 = net(p, n3)'; % 生成 4 x n3 的准随机点矩阵

volume3 = 1;
I3 = volume3 * sum(f3(X3)) / n3;

fprintf('采样点数: %d\n', n3);
fprintf('计算结果: %f\n', I3);
fprintf('书中结果 (n=10000): 1.06929...\n');
fprintf('结论: 等分布序列通常比伪随机数收敛更快，结果更接近精确值。\n\n');

%% --- 【例 6.4-4】: 五重积分问题 (书中此例有多个笔误) ---
% 我们将根据逻辑修正并求解
fprintf('--- 正在求解【例 6.4-4】: 五重积分 (修正版) ---\n');
% 积分区域:
% 0 < x1 < 1
% exp(x1)/2 < x2 < exp(x1)
% (x1+sin(x2))/2 < x3 < x1+sin(x2)
% (x1+x3)/2 < x4 < x1+x3
% x4 < x5 < x4+x1
%
% 被积函数: f(x) = sin(x1*exp(sqrt(x3))) + x4^x5 (书中为 x4*x5)

f4 = @(x) sin(x(1,:).*exp(sqrt(x(3,:)))) + x(4,:).^x(5,:); % 修正为 x4^x5，更复杂
n4 = 2000000; % 高维度问题需要更多点

% 定义包围盒 (根据书中和逻辑推断)
xmin4 = [0, exp(0)/2, -1/2, -1/2, -1/2]; % 考虑 sin 的范围
xmax4 = [1, exp(1),   2,      3,      4];
volume4 = prod(xmax4 - xmin4);

% 在包围盒内生成随机点
X4 = xmin4' + (xmax4' - xmin4') .* rand(5, n4);
x1_4=X4(1,:); x2_4=X4(2,:); x3_4=X4(3,:); x4_4=X4(4,:); x5_4=X4(5,:);

% 拒绝采样
ind4 = (x2_4 > exp(x1_4)/2) & (x2_4 < exp(x1_4)) & ...
       (x3_4 > (x1_4+sin(x2_4))/2) & (x3_4 < x1_4+sin(x2_4)) & ...
       (x4_4 > (x1_4+x3_4)/2) & (x4_4 < x1_4+x3_4) & ...
       (x5_4 > x4_4) & (x5_4 < x4_4+x1_4);

I4 = volume4 * sum(f4(X4(:, ind4))) / n4;

fprintf('采样点数: %d\n', n4);
fprintf('计算结果: %f\n', I4);